a=int(input("Enter a Number A :"))
b=int(input("Enter another Number:"))
c=input("Enter any operations +,-,/,*,%:")
if(c=='+'):
    print(a+b)
elif(c=='-'):
    print(a-b)
elif(c=='/'):
    print(a/b)
elif(c=='*'):
    print(a*b)
elif(c=='%'):
    print(a%b)
else:
    print("Invalid Operation")


            
